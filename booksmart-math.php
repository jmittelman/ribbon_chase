  <div class="project-description-text">

  <p>The website was built out using WordPress and was designed to make it easy to add to the database of learning resources through a custom post type that contained the relevant data fields. Ultimately, this allowed for presenting a list of resources that could be filtered and searched by criteria like resource type, grade level, and topic, as well as identifying editor-selected “BookSmart Picks.”</p>

    <p>Along with creating the layout and design for the functionality of website, I developed categories for blog articles to guide the overall plan for writing engaging content. Blog articles and other content pieces were developed to be distributed across social media channels and through email marketing including a “lead magnet” white paper that could be downloaded in exchange for a reader’s email address. Through these strategies, the website achieved a key result by attracting over 2000 subscribers to its email list in the first year of operation and setting the stage for continued growth.</p>

    <p>Ultimately, by sharing interactive tools to support and reinforce what children are already doing in school, the site enables parents to further unlock imagination and curiosity, allowing for deeper connections with the material. It helps them take an active role in their child’s learning, be self-assured as they guide their exploration of math, and, ultimately, feel proud for their growth and accomplishments. </p>

    

    

