  <p>Ultimately 500 people attended the event, alerted to it by in-store flyers and posters, email marketing, and stories in online and printed publications. They were encouraged to dress up and embrace a fashionable evening that included honky tonk and swing music, gin cocktails and snacks, and a special guest-- the Bike Snob, a prominent blogger.</p>

    <p>The stars of the show were four unique hand-built bicycles displayed for all to see that featured Brooks saddles and other accessories. And, the rest of the store space had been cleared out to exclusively display the full range of Brooks products, which attendees were encouraged to peruse and purchase.</p>

    <p>Brooks England’s customers have a refined taste for classic and stylish products but many bicycle stores find it tricky to carry their full range because they tend to promote more affordable, basic options made from plastic and rubber and/or components geared towards people who ride for sport. So, it can sometimes be hard to see and access the entire Brooks collection. </p>

    <p>Through the Dashing Bicycle Show, those who view cycling as a practical and elegant form of urban transport and are thereby attracted to Brooks’ mix of products full of retro charm were able to experience the full spirit and character of the brand and touch and try all of its products. And, by making this available to the public, BicycleSPACE succeeded in positioning itself as the place to go for the needs of practical and stylish urban transport.
 </p>

    

