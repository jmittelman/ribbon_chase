  <div class="project-description-text">

    <p>To spread the word, we placed posters at the old location and distributed flyers in addition to sending a number of informational emails to the existing store’s customers. And, with the new store set to open, we laid out a PR campaign, contacting media members and inviting them to an early-look VIP party, which resulted in numerous mentions and several articles in high-profile publications. Furthermore, I pounded the pavement to introduce myself and the company to local business owners, setting the stage for numerous partnerships that would develop in time. </p>

    <p>Ultimately, we planned to cap off the transition with a festive Grand Opening celebration which needed to excite the active crowd the store attracts. For this purpose, I created a number of activities including a Run Hunt—a photo-driven scavenger hunt that would task participants to run through the city following creative, rhyming clues to arrive at locations and take photos to document their completion of challenges. </p>

    <p>Teams of participants of the Run Hunt were judged on completion, creativity, and overall spirit. With 100 individual runners overall sharing photos on social media while tagging Pacers Running and using a common hashtag, it drove attention to the Grand Opening and the company’s dedication to celebrating the journey’s of all runners. </p>
  
