  <div class="project-description-text">

  <p>Brompton wanted to host a race in the US that would attract racers and brand fanatics from around the country. I worked with the brand and Washington, DC based bicycle shop BicycleSPACE to organize the race along with a slate of other events that flaunted the unique character of Brompton while positioning BicycleSPACE as a premier dealer and authority on all things Brompton.</p>

    <p>The weekend would include a party at the British Embassy, a social media activated scavenger hunt through the city, a happy hour event, and the championship race itself. These elements would reward participants for their brand loyalty and create promotional content that could convey the full joy of using a Brompton bicycle.</p>

    <p>While planning the event, I developed a sponsorship deck to seek support from other major brands. I created flyers and posters to mail to the country's Brompton dealers who could then help spread the word. I created an event site that highlighted all features of the race weekend and encouraged registration. I sent out emails that included promo videos to build excitement for visiting DC. </p>

    <p>Finally, I organized all logistics and coordinated every aspect of each event, leading a team of volunteers and coordinating with vendor partners. We built a race course, hosted parties, and celebrated all things Brompton. And, to share the experience we made a recap video that showed people embracing their love for these special bicycles and shared the full spirit of the brand. </p>

    

