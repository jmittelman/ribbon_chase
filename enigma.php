<div class="project-description-container" >

  <div class="project-description-text">

    <p>We first invited runners to attend a social run where they could demo Enigma shoes and receive a free Enigma pint glass from Mizuno along with the first puzzle piece. This was promoted by email and through posters placed in five of Pacers Running’s stores. The event saw nearly 200 participants attend who joined in the run led by representatives from Mizuno and Pacers Running. Upon completion of the route at a local brewery, runners were gifted their unique pint glass and puzzle pieces (printed flyers) while being instructed to visit a Pacers Running store for the next stage of the contest.</p>

    <p>At a store, runners could check out the Enigma shoes again as well as try the new Breath Thermo line of clothing. Upon their visit, they were given a second puzzle piece that revealed the url of a web page where they could enter a raffle for the grand prize—one of five head-to-toe clothing and shoe packages from Mizuno. The splash page also promoted the upcoming Jingle All the Way 5k race where participants would be able to further try out the Breath Thermo Clothing at a special ski lodge themed Mizuno demo event.</p>

    <p>Ultimately, while enabling extra fun and engagement for those who collected the puzzle pieces, the raffle was available to anyone who visited the Pacers Running stores and received several hundred entries resulting in positive promotion for Mizuno’s products and Pacers Running’s events. By serving as a helpful talking point, the contest helped the Pacers Running sales associates build excitement and interest around the Mizuno shoes and clothing and gave them a way to share info about upcoming social runs and races.  </p>
  
